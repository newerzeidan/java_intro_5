package print_statements;

public class PrintVsPrintln {
    public static void main(String[] args) {

        // println
        // println prints that given text and moves cursor to the next line
        System.out.println("This is println");
        System.out.println("Newer Zeidan");
        System.out.println("Today is Sunday");


        //print
        System.out.print("This is println");
        System.out.print("Newer Zeidan");
        System.out.print("Today is Sunday");

    }
}
